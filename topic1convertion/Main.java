package topic1convertion;
class Main {
    public static void main(String[] args) {
        // deaclare integer variable
        int num =10;
        System.out.println("num is " + num);


        // covert into double
        double data = (double)num;
        System.out.println("num is " + data);


        
        int another_num = (int)data;
        System.out.println("another_num is " + another_num);
    }
}